        <h2>Zone Pacifique Nord-Ouest : des « Jardins Forestiers » délibérément plantés par les populations
          autochtones !
        </h2>
        <div class="ref">
          <div style="width: 60px">
            <img src="../assets/img/q_002-logo-scienceMag.svg" alt="Logo du magazine Science" loading="lazy" />
          </div>
          <p>
            ANDREW CURRY 22/04/21 -
            <a href="https://www.science.org/content/article/pacific-northwest-s-forest-gardens-were-deliberately-planted-indigenous-people"
              title="article d'origine en anglais">article d'origine en anglais</a>
          </p>
        </div>
      </div>
      <figcaption>
        site du village ancestral Ts'msyen, dans le nord-ouest de la Colombie-Britannique. Il abrite toujours un mélange
        distinct d'espèces bénéfiques pour l'homme, au moins 150 ans après avoir été
        planté.
      </figcaption>
      <h3>Des recherches suggèrent que l’homme a apporté de la valeur à ces forêts de manière durable</h3>
      <div class="float right boxed">
        <div class="quote">
          “ des parcelles isolées d'arbres fruitiers et de buissons de baies (..) délibérément plantées par les peuples
          indigènes (..) capables de modifier les forêts de manière durable et productive. ”
        </div>
      </div>
      <p>
        Pendant des décennies, les Premières Nations de Colombie-Britannique (Canada) ont su que leurs maisons
        ancestrales - des villages vidés par la force à la fin des années 1800 - étaient des
        lieux privilégiés pour la recherche d'aliments traditionnels tels que les
        noisettes, les pommettes, les cranberries et les aubépines. Une nouvelle étude
        révèle que des parcelles isolées d'arbres fruitiers et de buissons de baies dans les forêts de ciguës et de
        cèdres de la région ont été
        délibérément plantées par les peuples indigènes dans et autour de leurs habitats, il y a plus de 150
        ans. C'est l'une des premières fois que de tels « jardins forestiers »
        ont été identifiés en dehors des tropiques, et cela montre que les populations étaient capables de
        modifier les forêts de manière durable et productive.
      </p>
      <p>
        « Il s'agit d'un travail très créatif et unique en son genre », déclare Kelly Kindscher, écologiste
        végétale à l'Université du Kansas, à Lawrence, qui n'a pas participé à la
        recherche. « Beaucoup d'entre nous savent qu'il y a des empreintes historiques sur la terre, mais ont
        tendance à ignorer les Amérindiens et les peuples autochtones en termes d'impact
        global. »
      </p>
      <div class="float left boxed">
        <div class="quote">
          “ beaucoup de temps aux chercheurs pour reconnaître qu'il s'agissait d'un paysage créé par l'homme (..) Sans
          un entretien constant, les écologistes supposaient que la forêt « naturelle » prendrait rapidement le
          dessus. ”
        </div>
      </div>
      <p>
        Comme ces jardins forestiers d'apparence sauvage ne correspondent pas aux notions occidentales
        conventionnelles d'agriculture, il a fallu
        beaucoup de temps aux chercheurs pour reconnaître qu'il s'agissait d'un paysage créé
        par l'homme. Jusqu'à récemment, de nombreux écologistes soutenaient que
        ces îlots de biodiversité, que l'on retrouve également dans les forêts tropicales humides d'Amérique centrale et
        d'Amérique du Sud, étaient un sous-produit accidentel et éphémère des
        incendies, des inondations ou du défrichement. Sans un entretien constant, les écologistes supposaient
        que la forêt « naturelle » prendrait rapidement le dessus.
      </p>
      <p>
        Pour démontrer que les jardins forestiers étaient le résultat de l'activité humaine, Chelsey Geralda Armstrong,
        écologiste historique de l'Université Simon Fraser, a d'abord identifié des
        sites de villages près de la ville de Vancouver, au Canada, et deux autres plus proches de l'Alaska, que les
        tribus locales ont été forcées d'abandonner à la fin du XIXe siècle.
      </p>
      <div class="float right boxed">
        <div class="quote">
          “ mélange de plantes beaucoup plus diversifié que les forêts de conifères environnantes ”
        </div>
      </div>
      <p>
        En comptant et en identifiant les espèces poussant sur et autour de ces anciens sites, elle a découvert qu'ils
        abritaient un mélange de plantes beaucoup plus diversifié que les forêts de
        conifères environnantes. Les espèces végétales occupaient également un plus grand nombre de niches écologiques.
        « Il est frappant de constater à quel point les jardins forestiers étaient différents de la forêt
        environnante, même après plus d'un siècle », explique Jesse Miller,
        biologiste à l'Université de Stanford et co-auteur de l'étude.
      </p>
      <p>
        Dans le même temps, les parcelles de terre exploitées il y a plusieurs décennies (par les
        colons) et laissées à elles-mêmes étaient couvertes de quelques espèces de
        conifères et ne présentaient pas le même catalogue d'espèces colorées et comestibles. « Les
        jardins forestiers s'inscrivent à contre-courant de la tendance », explique M.
        Armstrong.
      </p>
      <div class="float left boxed">
        <div class="quote">
          “ ils sont également restés résistants face à la flore locale dominante longtemps après que les gens ont
          quitté les lieux ”
        </div>
      </div>
      <p>
        Cela suggère que les jardins forestiers n'ont pas seulement été délibérément cultivés par les
        jardiniers indigènes, mais qu'ils sont également
        restés résistants face à la flore locale dominante longtemps après que les gens ont
        quitté les lieux, rapportent aujourd'hui les chercheurs dans Ecology and
        Society (Écologie et société). Le mélange d'espèces différentes a probablement joué un rôle clé dans
        leur persistance, explique M. Miller : « Il y a moins de niches
        ouvertes, et il est donc plus difficile pour les nouvelles espèces de venir s'y installer. »
      </p>
      <p>
        Les jardins forestiers étaient remplis de plantes bénéfiques pour l'homme, mais ils continuent
        également à fournir de la
        nourriture aux oiseaux, aux ours et aux insectes pollinisateurs, même après 150 ans de
        négligence. C'est la preuve que l'impact de l'homme sur l'environnement peut avoir des
        effets positifs durables. « Beaucoup d'études sur la diversité fonctionnelle ont une approche qui consiste à
        dire que l'homme est mauvais pour l'environnement », explique M. Armstrong.
        « Cette étude montre que les humains ont la capacité non seulement de permettre à la biodiversité de prospérer,
        mais aussi d'en faire partie. »
      </p>
      <p>
        D'autres chercheurs estiment que ces résultats pourraient contribuer à faire valoir l'importance des
        connaissances autochtones dans les efforts de conservation. « Les anthropologues et les
        archéologues ont plaidé en ce sens, mais les écologistes se sont montrés très réticents au cours des 20
        dernières années », explique Patrick Roberts, archéologue à
        l'Institut Max Planck pour la science de l'histoire humaine, qui n'a pas participé à l'étude. Selon lui, cette
        étude est une preuve importante que
        les modifications apportées par l'homme peuvent apporter une valeur ajoutée aux écosystèmes.
      </p>
      <div class="float right boxed">
        <div class="quote">
          “ contribue également à expliquer un mystère qui a intrigué de nombreux anthropologues (..) Malgré l'absence
          d'agriculture (..) les tribus rencontrées étaient socialement complexes, avec de grandes populations
          sédentaires et des sociétés hiérarchisées. ”
        </div>
      </div>
      <p>
        Elle contribue également à expliquer un mystère qui a intrigué de nombreux anthropologues
        européens lorsqu'ils ont visité pour la première fois le nord-ouest du Pacifique à
        la fin du XIXe siècle. Malgré l'absence de ce que les Européens considéraient comme
        « l'agriculture » - champs cultivés et cycles annuels de plantation et
        de récolte - les tribus qu'ils ont rencontrées étaient socialement complexes,
        avec de grandes populations sédentaires et des sociétés hiérarchisées. « Cela
        a déconcerté beaucoup d'anthropologues », qui pensaient que les formes occidentales d'agriculture étaient
        nécessaires à l'existence de sociétés complexes, explique M. Armstrong.
        « Aujourd'hui, nous savons qu'il n'y avait pas que le saumon. »
      </p>
